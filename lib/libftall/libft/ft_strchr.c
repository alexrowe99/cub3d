char	*ft_strchr(const char *s, int c)
{
	if (s)
	{
		while (*s != '\0')
		{
			if (*s == (char)c)
				return ((char *)s);
			s++;
		}
		if (*s == (char)c)
			return ((char *)s);
	}
	return ((void *)0);
}
